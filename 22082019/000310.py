# Creacion de una lista

num = [1, 2, 3, 4, 5, 6, 8, 9]

total = 1 # Se define una variable que permita trabajar con los elementos de la lista

for i in num:
    total = total*i # Se crea una funcion para trbajar con la lista en este caso multiplicacion
print (total)

