# Comando While, nos permite establecer una condicion de las variables, significa mientras

suma = 0 # Creamos la variable suma, que corresponde a la suma de los digitos
i = 1 # Se comienza con la variable 1 para ir sumando de a 1 d√≠gito

while i < 10: # Mientras i sea menor a 10 se suma 1 al valor de la lista
    suma += i
    i += 1
print (suma) # Finalmente se imprime 45 que es la suma de los numeros del 1 al 9


